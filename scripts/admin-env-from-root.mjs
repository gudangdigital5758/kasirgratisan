/**
 * Generate admin/.env.production from root .env (client-safe vars only).
 * Usage: node scripts/admin-env-from-root.mjs
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const rootEnvPath = resolve(root, '.env');
const outPath = resolve(root, 'admin', '.env.production');

function parseEnv(path) {
  if (!existsSync(path)) return {};
  const out = {};
  for (const line of readFileSync(path, 'utf8').split(/\r?\n/)) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const i = t.indexOf('=');
    if (i < 0) continue;
    const k = t.slice(0, i).trim();
    let v = t.slice(i + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    out[k] = v;
  }
  return out;
}

const env = parseEnv(rootEnvPath);
const api =
  process.env.ADMIN_API_URL ||
  'https://api.profitku.my.id';

const lines = [
  '# Generated by scripts/admin-env-from-root.mjs — do not commit secrets',
  `VITE_API_URL=${api}`,
  `VITE_SUPABASE_URL=${env.VITE_SUPABASE_URL || ''}`,
  `VITE_SUPABASE_ANON_KEY=${env.VITE_SUPABASE_ANON_KEY || ''}`,
  `VITE_GOOGLE_CLIENT_ID=${env.VITE_GOOGLE_CLIENT_ID || ''}`,
  '',
];

if (!env.VITE_SUPABASE_URL || !env.VITE_SUPABASE_ANON_KEY || !env.VITE_GOOGLE_CLIENT_ID) {
  console.error('Root .env missing VITE_SUPABASE_* or VITE_GOOGLE_CLIENT_ID');
  process.exit(1);
}

writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log('Wrote admin/.env.production');
console.log(`  VITE_API_URL=${api}`);
console.log(`  VITE_SUPABASE_URL=${env.VITE_SUPABASE_URL.slice(0, 30)}…`);
console.log(`  VITE_GOOGLE_CLIENT_ID length=${env.VITE_GOOGLE_CLIENT_ID.length}`);
